package com.carrent.vo;

public class BasicVo {

	String bis_code;
	String bis_coname;
	String bis_seq;
	String bis_rem;
	public String getBis_code() {
		return bis_code;
	}
	public void setBis_code(String bis_code) {
		this.bis_code = bis_code;
	}
	public String getBis_coname() {
		return bis_coname;
	}
	public void setBis_coname(String bis_coname) {
		this.bis_coname = bis_coname;
	}
	public String getBis_seq() {
		return bis_seq;
	}
	public void setBis_seq(String bis_seq) {
		this.bis_seq = bis_seq;
	}
	public String getBis_rem() {
		return bis_rem;
	}
	public void setBis_rem(String bis_rem) {
		this.bis_rem = bis_rem;
	}
	
}
